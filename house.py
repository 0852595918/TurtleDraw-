import turtle
from turtle import *
import math
from math import *
screen = turtle.Screen()
screen.setup(800,800)
ct = math.acos(100/135)*180/pi
print(ct)
pu()
goto(-300,-100)
pd()
color("black","blue")
begin_fill()
for i in range(2):
    fd(200)
    right(90)
    fd(100)
    right(90)
end_fill()
color("black","lime")
begin_fill()
left(float(ct))
fd(135)
right(180 - (180 - 2*ct))
fd(135)
end_fill()
color("black","fuchsia")
pu()
goto(-215,-200)
left(ct + 90)
pd()
begin_fill()
fd(50)
right(90)
fd(30)
right(90)
fd(50)
end_fill()
bgcolor("black")
pu()
goto(100,100)
pd()
colors = ["red", "yellow", "blue", "green"]
speed(100)
for x in range(200):
    pencolor(colors[x%4])
    forward(x)
    left(91)
screen.exitonclick()
